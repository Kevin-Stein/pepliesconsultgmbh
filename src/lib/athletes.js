// Ensure non-DE athlete text categories are fully populated.
// FR/ES keep their own entries, and automatically inherit EN as fallback base.
const ATHLETE_TEXT_CATEGORIES = ["achievements", "interests", "hobbies"];
["fr", "es"].forEach((localeKey) => {
  ATHLETE_TEXT_CATEGORIES.forEach((categoryKey) => {
    translations[localeKey][categoryKey] = {
      ...(translations.en[categoryKey] || {}),
      ...(translations[localeKey][categoryKey] || {}),
    };
  });
});

const SUPPORTED_ATHLETE_LANGS = ["de", "en", "fr", "es"];

// Function to get athletes in the specified language
export const getAthletes = (language = "de") => {
  const lang = SUPPORTED_ATHLETE_LANGS.includes(language) ? language : "de";
  const langTranslations = translations[lang] || translations.en;

  const pick = (category, key) => {
    if (!key) return key;
    // German is our source dataset language for most free-text fields.
    // Do not fall back to English when locale is "de".
    if (lang === "de") {
      return langTranslations[category]?.[key] ?? key;
    }

    // For non-DE locales: prefer locale-specific mapping, then English, then raw source text.
    return langTranslations[category]?.[key] ?? translations.en[category]?.[key] ?? key;
  };

  return athletes.map((athlete) => {
    const localPortrait = getLocalAthleteImage(athlete.firstName, athlete.lastName, "portrait");
    const localAction = getLocalAthleteImage(athlete.firstName, athlete.lastName, "action");
    const fullName = `${athlete.firstName} ${athlete.lastName}`;
    const photoCredits = {
      ...(athlete.photoCredits || {}),
      ...(photoCreditsByAthlete[fullName] || {}),
    };

    const translatedAchievements = (athlete.achievements || []).map((achievement) => pick("achievements", achievement));

    const translatedInterests = (athlete.interests || []).map((interest) => pick("interests", interest));

    const translatedHobby = athlete.hobby ? pick("hobbies", athlete.hobby) : "";

    let translatedProfession = athlete.profession || "";
    if (athlete.profession) {
      const mapped = pick("professions", athlete.profession);
      if (mapped !== athlete.profession) {
        translatedProfession = mapped;
      } else {
        const professionParts = athlete.profession.split(", ");
        if (professionParts.length > 1) {
          const firstPart = professionParts[0];
          const rest = professionParts.slice(1).join(", ");
          const firstMapped = pick("professions", firstPart);
          if (firstMapped !== firstPart) {
            translatedProfession = `${firstMapped}, ${rest}`;
          } else {
            translatedProfession = athlete.profession;
          }
        } else {
          translatedProfession = athlete.profession;
        }
      }
    }

    const disc = athlete.sportDiscipline ? pick("sportDisciplines", athlete.sportDiscipline) : athlete.sportDiscipline;

    return {
      ...athlete,
      portraitImageURL: localPortrait || athlete.portraitImageURL,
      actionImageURL: localAction || athlete.actionImageURL,
      photoCredits,
      sportDiscipline: disc || athlete.sportDiscipline,
      profession: translatedProfession,
      achievements: translatedAchievements,
      interests: translatedInterests,
      hobby: translatedHobby,
    };
  });
};

// Default export for backward compatibility
export default athletes;
